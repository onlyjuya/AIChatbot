// 기본 성격 설정
export const DEFAULT_PERSONALITY = PERSONALITIES.friendly;

/**
 * 성격 ID로 성격 객체를 가져오는 헬퍼 함수
 */
export function getPersonality(id: string): Personality {
  return PERSONALITIES[id] || DEFAULT_PERSONALITY;
}

/**
 * 모든 성격 목록을 배열로 반환
 */
export function getAllPersonalities(): Personality[] {
  return Object.values(PERSONALITIES);
}

/**
 * 시스템 프롬프트를 가져오는 함수
 */
export function getSystemPrompt(personalityId: string): string {
  const personality = getPersonality(personalityId);
  return personality.systemPrompt;
}
